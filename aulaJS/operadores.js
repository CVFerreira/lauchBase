//operadores relacionais

/* 
>  - Maior que
<  - Menor que
>=  - Maior ou igual a..
<=  - Menor ou igual a..
 */

 const a = 5
 const b = 6

 console.log(a > b);
 console.log(a < b);
 console.log(a >= b);
 console.log(a <= b);

 //Operadores de igualdade
 /* 
 == - igual
 != - diferente de
 === - igual e do mesmo tipo
 !== - diferente inclusive o tipo
 */

 const c = 5
 const d = '5'
 
 console.log(c == d);
 console.log(c != d);
 console.log(c === d);
 console.log(c !== d);
 
 //Operadores Lógicos
 /*
 && - Compara duas condições, se ambas forem verdadeiras
 || - Compara duas condições, se uma das duas forem verdadeiras
 ! - Vai negar uma expressão
 */

 const e = 5
 const f = '5'

