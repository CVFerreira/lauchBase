 console.log(e == f && e != b);

 console.log(e == f || e !== b);
 console.log(e == f || e != b);
 console.log(e != f || e === b);

 console.log(!(e == f || e != b));
 
 //operadores aritimeticos
 /*
 * - multiplicação
 / - divisão
 % - resto da divisão
 + - adição
 - - subtração
 */

 console.log(2 * 2);
 console.log(2 / 2);
 console.log(2 % 1.4);
 console.log(2 + 2);
 console.log(2 - 2);
 


 
 